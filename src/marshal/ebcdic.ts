// Build reverse mapping: ASCII -> EBCDIC for CP037
const ASCII_TO_CP037: number[] = new Array(256).fill(0x3F); // default to EBCDIC '?'
for (let i = 0; i < 256; i++) {
  const asciiVal = CP037_TO_ASCII[i];
  if (asciiVal !== 0x1A) {
    ASCII_TO_CP037[asciiVal] = i;
  }
}

// Differences from CP037 for Code Page 500 (International)
// Only the positions that differ are listed
const CP500_DIFFS: Record<number, number> = {
  0x4A: 0xA2, // cent sign
  0x4F: 0x7C, // vertical bar
  0x5A: 0x21, // exclamation
  0x5B: 0x24, // dollar (moved)
  0x5F: 0xAC, // not sign
  0x6A: 0xA6, // broken bar
  0xB0: 0x5E, // caret
  0xBA: 0x5B, // left bracket
  0xBB: 0x5D, // right bracket
};

// Differences from CP037 for Code Page 1047 (Latin 1/Open Systems)
const CP1047_DIFFS: Record<number, number> = {
  0x15: 0x0A, // newline (NL -> LF)
  0x25: 0x0A, // line feed
  0xAD: 0x5B, // left bracket
  0xBD: 0x5D, // right bracket
  0xC0: 0x7B, // left brace
  0xD0: 0x7D, // right brace
  0xE0: 0x5C, // backslash
};

/**
 * Build a full 256-byte translation table for a given code page
 * by applying diffs on top of the base CP037 table.
 */
function buildCodePageTable(diffs: Record<number, number>): number[] {
  const table = [...CP037_TO_ASCII];
  for (const [ebcdic, ascii] of Object.entries(diffs)) {
    table[Number(ebcdic)] = ascii;
  }
  return table;
}

/**
 * Build a reverse (ASCII -> EBCDIC) table from a forward table.
 */
function buildReverseTable(forwardTable: number[]): number[] {
  const reverse = new Array(256).fill(0x3F); // default to EBCDIC '?'
  for (let i = 0; i < 256; i++) {
    const asciiVal = forwardTable[i];
    if (asciiVal !== 0x1A) {
      reverse[asciiVal] = i;
    }
  }
  return reverse;
}

// Precomputed tables for each code page
const CODE_PAGE_TABLES: Record<CodePage, { toAscii: number[]; toEbcdic: number[] }> = {
  '037': {
    toAscii: CP037_TO_ASCII,
    toEbcdic: ASCII_TO_CP037,
  },
  '500': (() => {
    const t = buildCodePageTable(CP500_DIFFS);
    return { toAscii: t, toEbcdic: buildReverseTable(t) };
  })(),
  '1047': (() => {
    const t = buildCodePageTable(CP1047_DIFFS);
    return { toAscii: t, toEbcdic: buildReverseTable(t) };
  })(),
};

/**
 * Convert an EBCDIC-encoded buffer to an ASCII string.
 *
 * @param buffer - Buffer containing EBCDIC-encoded data
 * @param codePage - EBCDIC code page to use (default: '037')
 * @returns ASCII string
 */
export function ebcdicToAscii(buffer: Buffer, codePage: CodePage = '037'): string {
  const table = CODE_PAGE_TABLES[codePage];
  if (!table) {
    throw new Error(`Unsupported code page: ${codePage}`);
  }

  const result = Buffer.alloc(buffer.length);
  for (let i = 0; i < buffer.length; i++) {
    const asciiVal = table.toAscii[buffer[i]];
    // Untranslatable characters (0x1A = SUB) become '?' (0x3F)
    result[i] = asciiVal === 0x1A ? 0x3F : asciiVal;
  }
  return result.toString('latin1');
}

/**
 * Convert an ASCII string to an EBCDIC-encoded buffer.
 *
 * @param str - ASCII string to convert
 * @param codePage - EBCDIC code page to use (default: '037')
 * @returns Buffer containing EBCDIC-encoded data
 */
export function asciiToEbcdic(str: string, codePage: CodePage = '037'): Buffer {
  const table = CODE_PAGE_TABLES[codePage];
  if (!table) {
    throw new Error(`Unsupported code page: ${codePage}`);
  }

  const input = Buffer.from(str, 'latin1');
  const result = Buffer.alloc(input.length);
  for (let i = 0; i < input.length; i++) {
    result[i] = table.toEbcdic[input[i]];
  }
  return result;
}
